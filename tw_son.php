<h1>羽毛球规则</h1>
<div id="ph">
<p>
国际羽联在马来西亚首都吉隆玻正式宣布新规则从2006年2月1日起正式实施。</p>
<p>国际羽联新的计分规则最大变化是取消了发球得分制，实行每球得分制，另外将所有单项的每局获胜分统一定为21分。新的计分规则具体规定如下：</p>
<strong>单打比赛</strong>
<p>1、每场比赛采取三局两胜制；</p>
<p>2、率先得到21分的一方赢得当局比赛；</p>
<p>3、如果双方比分打成20比20，获胜一方需超过对手2分才算取胜；</p>
<p>4、如果双方比分打成29比29，则率先得到第30分的一方取胜；</p>
<p>5、首局获胜一方在接下来的一局比赛中率先发球；</p>
<p>6、当一方在比赛中得到11分后，双方队员将休息1分钟；</p>
<p>7、两局比赛之间的休息时间为2分钟；</p>
<strong>双打比赛特殊规则</strong>
<p>1、改双发球权为单发球权；</p>
<p>2、后发球线保留，现行规则适用；</p>
<p>3、比赛开始前，双方选手通过投掷硬币方式确定由哪一方来选择是先发球或后发球；</p>
<p>例子： http://www.badmintoncn.com/view-3464-1.html</p>
<p>如果双方在A/B一对组合和C/D组合之间进行，A/B一方选择先发球。假如说A站在两人的右手区域，那么A先发球给对角线位置上的C(假设)，如果A/B一方得分，那么A和B需要交换彼此的站位元区，还有由A来发球，将球发给D(A/B一方得分C和D两人不换位置)；</p>
<br />
<p>如果此时C/D一方得分，那么双方四名队员都不换位置，发球权交给C/D一方，由刚才接发球的D来发球，D发球给对方刚才发球的选手A；</p>
<p>如果D发球后C/D一方得分，那么C和D交换位置继续由D发球给B；如果D发球后得分的是A/B一方，那么双方队员不用换位，发球权交给B。</p>
<p>总的来说双打比赛除了改一方两次发球权为一次发球权之外，其他规则与原规则类似，没有较大变化，球迷看球时不会感到不适应，也许比赛进行速度和分数增长速度加快才是观众最初看比赛最不适应的。</p>
